def flipY(frameData, frameNum):
   frameData[frameNum] = frameData[frameNum][::-1]
   return frameData

def flipX(frameData, frameNum):
   result = []
   for i in range(len(frameData[frameNum])):
      result.append(frameData[frameNum][i][::-1])
   frameData[frameNum] = result
   return frameData

def rotate90(frameData, frameNum):
   frameSize = len(frameData[frameNum])
   result = []
   for i in range(frameSize):
      result.append('')
   #print frameData[frameNum]
   for i in range(frameSize):
      for j in range(frameSize):
         result[frameSize-1-j] += frameData[frameNum][i][j]
   #print result
   frameData[frameNum] = result
   return frameData

def compareCenterFrame(frameData, frame1, frame2):
   frame1Chars = ''
   frame2Chars = ''
   frameSize = len(frameData[frame1])
   for i in range(frameSize):
      frame1Chars += frameData[frame1][i][-1]
      frame2Chars += frameData[frame2][i][0]
   return frame1Chars == frame2Chars

def compareMidFrame(frameData, frame1, frame2):
   frame1Chars = frameData[frame1][-1]
   frame2Chars = frameData[frame2][0]
   return frame1Chars == frame2Chars

def determineNeighbours(x, y, tileNeighbours, grid):
   if x == y and x == 0:
      return tileNeighbours[grid[0][0]][0], tileNeighbours[grid[0][0]][1]
   elif x == y and x < len(grid):
      #print grid[y][x], grid[y-1][x]
      return getTileBasedOnIndirectNeighbours(grid[y][x], grid[y-1][x], tileNeighbours, grid), getTileBasedOnIndirectNeighbours(grid[y][x], grid[y][x-1], tileNeighbours, grid), 
   return True, True

def getTileBasedOnNeighbours(neigh1, neigh2, tileNeighbours, grid):
   #print neigh1, neigh2
   for k in tileNeighbours.keys():
      if k in tileNeighbours[neigh1] and k in tileNeighbours[neigh2] and not any(k in vals for vals in grid):
         return k
   return True

def getTileBasedOnIndirectNeighbours(neigh1, neigh2, tileNeighbours, grid):
   #print neigh1, neigh2
   for n in [x for x in tileNeighbours[neigh2] if  not any(x in vals for vals in grid)]:
      for k in tileNeighbours[n]:
         if k in tileNeighbours[neigh1] and k in tileNeighbours[n] and not any(k in vals for vals in grid):
            return k
   return True

