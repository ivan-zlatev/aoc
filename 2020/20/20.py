def searchForMonster(waters, monsterPattern):
   monstersFound = 0
   coords = []
   for row in range(0, len(waters)-len(monsterPattern)+1):
      for col in range(0, len(waters[0])):
         match = [False]*len(monsterPattern)
         for i in range(len(match)):
            if re.match(monsterPattern[i], waters[row+i][col:]):
               match[i] = True
         if False not in match:
            monstersFound += 1
            coords.append([[row, col], [row+len(monsterPattern)-1, col+len(monsterPattern[0])-1]])
   return monstersFound, coords

