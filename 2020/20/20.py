def combinePictureArray(grid, frameData):
   result = []
   for row in grid:
      data = []
      for i in row:
         data.append([x[1:-1] for x in frameData[i][1:-1]])
      for k in range(len(data[0])):
         newRow = ''
         for i in range(len(data)):
            newRow += data[i][k]
         result.append(newRow)
   return result

def parseInputData(tile, tiles, frameData):
   tileAddr = int(tile[0].split(' ')[1].split(':')[0])
   tileData = tile[1:]
   tileSides = ['']*4
   tileSides[0] = tileData[0]
   tileSides[2] = tileData[-1]
   frameData[tileAddr] = []
   for row in tileData:
      frameData[tileAddr].append(row)
      tileSides[1] += row[-1]
      tileSides[3] += row[0]
   tiles[tileAddr] = generateFlipped(tileSides)
   return frameData

def generateFlipped(sides):
   for i in range(len(sides)):
      sides.append(sides[i][::-1])
   return sides

